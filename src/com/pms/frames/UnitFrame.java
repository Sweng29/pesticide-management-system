    private void searchUnitKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchUnitKeyReleased
        String txt = searchUnit.getText().toString();
        TableRowSorter trs = new TableRowSorter(unitTable.getModel());
        unitTable.setRowSorter(trs);       
        trs.setRowFilter(RowFilter.regexFilter("(?i)"+txt));
    }//GEN-LAST:event_searchUnitKeyReleased

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        int unitId = (int) unitTable.getModel().getValueAt(getRowIndex(), 0);
        if(new UnitDAOImpl().deleteUnit(unitId) == 1){         
           JOptionPane.showMessageDialog(this, "Record Deleted"); 
           DefaultTableModel dtm = (DefaultTableModel)unitTable.getModel();
           dtm.removeRow(getRowIndex());
           dtm.fireTableDataChanged();
        }
    }//GEN-LAST:event_DeleteActionPerformed

