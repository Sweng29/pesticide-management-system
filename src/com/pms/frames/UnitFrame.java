    private void addData(){
       if(addNameField.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Fields should not be empty");
       }
       else{
           
        UnitModel unitModel = new UnitModel();
        unitModel.setUnitName(addNameField.getText());
        unitModel.setCreatedBy(LoginFrame.userModel);
        unitModel.setCreatedDate(new Timestamp(System.currentTimeMillis()));
        unitModel.setModifiedBy(LoginFrame.userModel);
        unitModel.setModifiedDate(new Timestamp(System.currentTimeMillis()));
        
        if(new UnitDAOImpl().addUnit(unitModel) == 1){
            JOptionPane.showMessageDialog(unitAddFrame, "Record Added!");
        }
        
        fillDataIntoUnitTable();
        unitAddFrame.dispose();
           
       }
    }
    
    private void updateData(){
        
        if(nameField.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Fields should not be empty");
           
       }
        else{
            
            DefaultTableModel dtm = (DefaultTableModel) unitTable.getModel();       
            dtm.setValueAt(nameField.getText(),getRowIndex(), 1);
        
            UnitModel unitModel = new UnitModel();
        
            unitModel.setUnitId((Integer) dtm.getValueAt(getRowIndex(), 0));
            unitModel.setUnitName((String) dtm.getValueAt(getRowIndex(), 1));
            unitModel.setModifiedBy(LoginFrame.userModel);
            unitModel.setModifiedDate(new Timestamp(System.currentTimeMillis()));
        
            if(new UnitDAOImpl().updateUnit(unitModel) == 1){
            JOptionPane.showMessageDialog(updateUnit, "Record Updated!");
            fillDataIntoUnitTable();
            searchUnit.setText("");
            }
            
            updateUnit.dispose();
            
            
        }
    }
