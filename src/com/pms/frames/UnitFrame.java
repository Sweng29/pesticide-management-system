    private void unitTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_unitTableMouseClicked
        
        int row = unitTable.getSelectedRow();
        
        if(row > -1){
           row = unitTable.convertRowIndexToModel(row);  
            setRowIndex(row);
      
        }
        
        if(evt.getClickCount() == 2){
            updateUnit.setVisible(true);
            updateUnit.setSize(new Dimension(440,281));
            updateUnit.setLocationRelativeTo(unitTable);
            
            int unitId = (int)unitTable.getModel().getValueAt(getRowIndex(), 0);
            
            UnitModel unitModel = new UnitDAOImpl().getUnitWithId(unitId);
            nameField.setText(unitModel.getUnitName());
            
            
        }
        
    }//GEN-LAST:event_unitTableMouseClicked

