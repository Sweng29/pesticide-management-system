    private boolean checkValidityForUpdate(){
        if(!LoginFrame.checkStringValidity(nameField.getText())){
            JOptionPane.showMessageDialog(this, "Name Field Contains only Alphabet letters");
            return false;
        }
        
        if(contactField.getText().trim().length() != 14){
            JOptionPane.showMessageDialog(this, "Invalid Contact");
            return false;
        }
        return true;
    }
    
    private boolean checkValidityForInsert(){
        if(!LoginFrame.checkStringValidity(addNameField.getText())){
            JOptionPane.showMessageDialog(this, "Name Field Contains only Alphabet letters");
            return false;
        }
        
        if(addContactField.getText().trim().length() != 14){
            JOptionPane.showMessageDialog(this, "Invalid Contact");
            return false;
        }
        return true;
    }
    
