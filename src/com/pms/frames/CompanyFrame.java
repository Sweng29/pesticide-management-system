    private void addData(){
        
        if(addNameField.getText().equals("") || addAddressArea.getText().equals("") || addContactField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Any Field Could not be empty");
        }
        else{
           if(checkValidityForInsert()){
            CompanyModel companyModel = new CompanyModel(); 
            companyModel.setName(addNameField.getText());
            companyModel.setAddress(addAddressArea.getText());
            companyModel.setContact(addContactField.getText());
            companyModel.setCreatedBy(LoginFrame.userModel);
            companyModel.setCreatedDate(new Timestamp(System.currentTimeMillis()));
            companyModel.setModifiedBy(LoginFrame.userModel);
            companyModel.setModifiedDate(new Timestamp(System.currentTimeMillis()));
        
        if(new CompanyDAOImpl().addCompany(companyModel) == 1){
            JOptionPane.showMessageDialog(companyFrame, "Record Added!");
            fillDataIntoCompanyTable();
        }
        
        companyFrame.dispose();
           }
        }
        
    }
    
